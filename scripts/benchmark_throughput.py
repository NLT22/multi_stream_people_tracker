"""
Camera throughput benchmark — measures sustainable FPS at various stream counts.

Runs the main pipeline in headless mode (fakesink, no display) with synthetic
sources (one real video looped N times) and parses the built-in FPS probe
output to report per-stream and total FPS.

Usage:
    python scripts/benchmark_throughput.py \\
        --source dataset/Wildtrack/cam1.mp4 \\
        [--cam-counts 1 2 4 6 8 10 12 16 20] \\
        [--duration 30]          # seconds per run
        [--warmup 5]             # seconds to discard (engine warm-up)
        [--target-fps 10]        # mark pass/fail per run
        [--nvinfer-config configs/models/nvinfer_yolov11_people.yml]
        [--gpu-id 0]

Output:
    Console table + CSV at output/benchmark/throughput_<timestamp>.csv
"""

from __future__ import annotations

import argparse
import csv
import re
import shutil
import subprocess
import sys
import threading
import time
from datetime import datetime
from pathlib import Path

import yaml


# Regex matching the FPS probe line: **FPS:  49.99 (49.99)
_FPS_RE = re.compile(r"\*\*FPS:\s+([\d.]+)\s+\(([\d.]+)\)")


# ---------------------------------------------------------------------------
# GPU monitoring via nvidia-smi
# ---------------------------------------------------------------------------

def _query_gpu(gpu_id: int) -> dict:
    """Return current GPU stats as a dict. Returns zeros on failure."""
    try:
        out = subprocess.check_output(
            [
                "nvidia-smi",
                f"--id={gpu_id}",
                "--query-gpu=name,memory.total,memory.used,"
                "utilization.gpu,temperature.gpu,power.draw",
                "--format=csv,noheader,nounits",
            ],
            text=True, stderr=subprocess.DEVNULL,
        ).strip()
        parts = [p.strip() for p in out.split(",")]
        def _int(v):   return int(v)   if v not in ("N/A", "[N/A]", "") else 0
        def _float(v): return float(v) if v not in ("N/A", "[N/A]", "") else 0.0
        return {
            "gpu_name":      parts[0],
            "vram_total_mb": _int(parts[1]),
            "vram_used_mb":  _int(parts[2]),
            "gpu_util_pct":  _int(parts[3]),
            "temp_c":        _int(parts[4]),
            "power_w":       _float(parts[5]),
            "power_limit_w": 0.0,
        }
    except Exception:
        return {
            "gpu_name": "unknown", "vram_total_mb": 0,
            "vram_used_mb": 0, "gpu_util_pct": 0,
            "temp_c": 0, "power_w": 0.0, "power_limit_w": 0.0,
        }


class _GpuMonitor:
    """Background thread that samples GPU stats every interval_s seconds."""

    def __init__(self, gpu_id: int, interval_s: float = 1.0):
        self._gpu_id   = gpu_id
        self._interval = interval_s
        self._samples: list[dict] = []
        self._stop     = threading.Event()
        self._thread   = threading.Thread(target=self._run, daemon=True)

    def start(self) -> "_GpuMonitor":
        self._thread.start()
        return self

    def stop(self) -> list[dict]:
        self._stop.set()
        self._thread.join(timeout=5)
        return self._samples

    def _run(self) -> None:
        while not self._stop.is_set():
            self._samples.append(_query_gpu(self._gpu_id))
            self._stop.wait(self._interval)

    def summary(self) -> dict:
        if not self._samples:
            return {}
        def _avg(key):
            return round(sum(s[key] for s in self._samples) / len(self._samples), 1)
        def _max(key):
            return max(s[key] for s in self._samples)
        return {
            "gpu_name":          self._samples[0]["gpu_name"],
            "vram_total_mb":     self._samples[0]["vram_total_mb"],
            "vram_used_mean_mb": _avg("vram_used_mb"),
            "vram_used_peak_mb": _max("vram_used_mb"),
            "gpu_util_mean_pct": _avg("gpu_util_pct"),
            "gpu_util_peak_pct": _max("gpu_util_pct"),
            "temp_peak_c":       _max("temp_c"),
            "power_mean_w":      _avg("power_w"),
            "power_limit_w":     self._samples[0]["power_limit_w"],
        }

# Regex for per-stream FPS when multiple streams: **FPS: 12.34 (12.34) Stream 0: ...
# Fall back to single value when only one stream.


def _parse_fps_lines(lines: list[str]) -> list[float]:
    """Extract all instantaneous FPS readings from captured output lines."""
    fps_values = []
    for line in lines:
        m = _FPS_RE.search(line)
        if m:
            fps_values.append(float(m.group(1)))   # instantaneous (first number)
    return fps_values


def _patch_nvinfer_interval(base_config: str, interval: int, gpu_id: int) -> str:
    """Return path to a patched nvinfer config with the given interval value.

    Writes a sibling file  <stem>_interval<N>.yml  next to the original.
    The runtime_b*_gpu*.yml generated by src.main will be derived from this.
    interval=0 means run inference on every frame (default behaviour).
    """
    src = Path(base_config)
    raw = yaml.safe_load(src.read_text()) or {}
    prop = raw.setdefault("property", {})
    prop["interval"] = interval
    raw["property"] = prop

    out = src.with_name(f"{src.stem}_iv{interval}.yml")
    with open(out, "w") as f:
        yaml.dump(raw, f, default_flow_style=False, sort_keys=False)
    return str(out)


def _get_video_duration(path: str) -> float:
    """Return video duration in seconds via ffprobe, or 0 on failure."""
    try:
        out = subprocess.check_output(
            ["ffprobe", "-v", "quiet", "-show_entries", "format=duration",
             "-of", "default=noprint_wrappers=1:nokey=1", path],
            text=True, stderr=subprocess.DEVNULL,
        ).strip()
        return float(out)
    except Exception:
        return 0.0


def _make_looped_uri(source: str, min_seconds: float) -> str:
    """Return a file:// URI to a video that is at least min_seconds long.

    If the source is already long enough, return it as-is.
    Otherwise loop it via ffmpeg concat to create a cached clip.
    """
    import shutil

    # Get duration of source
    try:
        out = subprocess.check_output(
            ["ffprobe", "-v", "quiet", "-show_entries", "format=duration",
             "-of", "default=noprint_wrappers=1:nokey=1", source],
            text=True, stderr=subprocess.DEVNULL,
        ).strip()
        src_duration = float(out)
    except Exception:
        src_duration = 0.0

    src_path = Path(source.replace("file://", ""))
    if src_duration >= min_seconds:
        return f"file://{src_path.resolve()}"

    # Need to loop: calculate how many repeats
    repeats = int(min_seconds / max(src_duration, 1)) + 2
    cache_dir = Path("output/_bench_loop")
    cache_dir.mkdir(parents=True, exist_ok=True)
    loop_path = cache_dir / f"{src_path.stem}_loop{repeats}x.mp4"

    if not loop_path.exists():
        # Write concat list file
        concat_list = cache_dir / f"{src_path.stem}_list.txt"
        concat_list.write_text(
            "\n".join([f"file '{src_path.resolve()}'"] * repeats) + "\n"
        )
        ffmpeg = shutil.which("ffmpeg") or "ffmpeg"
        subprocess.run(
            [ffmpeg, "-y", "-f", "concat", "-safe", "0",
             "-i", str(concat_list),
             "-c", "copy", str(loop_path)],
            check=True, capture_output=True,
        )
        print(f"  [bench] Created looped source: {loop_path} "
              f"({repeats}× {src_duration:.0f}s = {repeats*src_duration:.0f}s)")
    else:
        print(f"  [bench] Reusing looped source: {loop_path}")

    return f"file://{loop_path.resolve()}"


def _run_single(
    source: str,
    n_cams: int,
    duration: int,
    warmup: int,
    nvinfer_config: str,
    tracker_config: str,
    gpu_id: int,
    inference_interval: int,
) -> dict:
    """Run pipeline with n_cams copies of source, return timing stats.

    With sync=0 a 120s video can finish in <1s wall time at 200fps, before
    measure_fps_probe (which fires every 5s wall clock) gets any readings.
    We need the video to contain enough *frames* that the pipeline runs for
    at least (warmup + duration + margin) wall-clock seconds.

    Estimate: assume pipeline processes at most ~300fps per camera batch.
    So we need video_frames >= 300 * (warmup + duration + 10).
    Equivalently: video_seconds >= (warmup + duration + 10) * 300 / src_fps.
    We use a conservative 400fps upper bound so the clip is always long enough.
    """
    src_path = Path(source.replace("file://", ""))
    src_duration = _get_video_duration(str(src_path))
    src_fps = 30.0  # conservative assumption; actual may be higher
    try:
        out = subprocess.check_output(
            ["ffprobe", "-v", "quiet", "-show_entries", "stream=r_frame_rate",
             "-select_streams", "v:0",
             "-of", "default=noprint_wrappers=1:nokey=1", str(src_path)],
            text=True, stderr=subprocess.DEVNULL,
        ).strip().split("\n")[0]
        num, den = out.split("/")
        src_fps = float(num) / float(den)
    except Exception:
        pass

    # FPS probe fires every 5s wall time. We need the video to last at least
    # (warmup + duration + 15s) wall seconds.
    # With sync=0 at ~300fps, each video second ≈ 1/300 wall second, so:
    #   video_seconds_needed = wall_needed * pipeline_fps / src_fps
    # We use 500fps as conservative upper bound so the clip is always enough.
    wall_needed = warmup + duration + 15
    max_fps_estimate = 500.0
    video_seconds_needed = wall_needed * max_fps_estimate / max(src_fps, 1.0)

    loop_uri = _make_looped_uri(source, video_seconds_needed)

    # Patch nvinfer config with the requested inference interval
    patched_config = _patch_nvinfer_interval(nvinfer_config, inference_interval, gpu_id)

    # Build source list: repeat the same looped clip n_cams times
    sources = [loop_uri] * n_cams

    cmd = [
        sys.executable, "-m", "src.main",
        "--sources", *sources,
        "--no-display",
        "--no-sync",
        "--nvinfer-config", patched_config,
        "--tracker-config", tracker_config,
        "--gpu-id", str(gpu_id),
        "--no-tiler",       # skip tiler for pure throughput measurement
        "--disable-gallery",
        "--disable-osd",
    ]

    iv = inference_interval
    print(f"\n[bench] Running: n_cams={n_cams}  interval={iv}"
          f"  (infer every {iv+1}f)  warmup={warmup}s  measure={duration}s")

    start_wall = time.monotonic()
    deadline   = start_wall + wall_needed
    gpu_mon = _GpuMonitor(gpu_id, interval_s=1.0).start()

    proc = subprocess.Popen(
        cmd,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True,
        cwd=str(Path(__file__).parent.parent),
    )

    fps_readings: list[float] = []
    lines: list[str] = []

    _is_tty = sys.stdout.isatty()
    _live_lines_printed = 0

    def _print_live(readings: list[float], g: dict) -> None:
        """Update live status — rewrite in-place on TTY, append line on pipe."""
        nonlocal _live_lines_printed

        elapsed = time.monotonic() - start_wall
        phase   = "warm-up" if elapsed < warmup else "measuring"

        fps_str = ""
        if readings:
            fps_val  = readings[-1]
            fps_mean = sum(readings) / len(readings)
            fps_str  = (f"  FPS={fps_val:.0f}  mean={fps_mean:.0f}"
                        f"  per-cam={fps_val/max(1,n_cams):.0f}")

        gpu_str = ""
        if g["vram_total_mb"]:
            gpu_str = (f"  VRAM={g['vram_used_mb']}MB"
                       f"  GPU={g['gpu_util_pct']}%"
                       f"  {g['temp_c']}°C")

        status = (f"  [{phase}] {elapsed:.0f}s / {warmup+duration}s"
                  f"  samples={len(readings)}{fps_str}{gpu_str}")

        if _is_tty and _live_lines_printed:
            sys.stdout.write("\033[1A\033[2K")   # up 1 line, erase it
        print(status)
        sys.stdout.flush()
        _live_lines_printed = 1

    try:
        for raw_line in proc.stdout:
            line = raw_line.rstrip()
            lines.append(line)

            if time.monotonic() >= deadline:
                proc.terminate()
                break

            m = _FPS_RE.search(line)
            if m:
                elapsed = time.monotonic() - start_wall
                fps_val = float(m.group(1))
                if elapsed >= warmup:
                    fps_readings.append(fps_val)
                g = _query_gpu(gpu_id)
                _print_live(fps_readings, g)

            elif "ERROR" in line or "Segmentation" in line:
                # Always surface errors even in quiet mode
                print(f"\n  [!] {line}")
                _live_lines_printed = 0

    except KeyboardInterrupt:
        proc.terminate()

    # Clear live block before printing final summary line
    if _live_lines_printed:
        sys.stdout.write(f"\033[{_live_lines_printed}A\033[J")
        _live_lines_printed = 0

    proc.terminate()
    proc.wait(timeout=5)
    wall_time = time.monotonic() - start_wall
    time.sleep(0.5)   # let GPU monitor collect one final sample
    gpu_samples = gpu_mon.stop()

    monitor_summary = {}
    if gpu_samples:
        def _avg(k): return round(sum(s[k] for s in gpu_samples) / len(gpu_samples), 1)
        def _mx(k):  return max(s[k] for s in gpu_samples)
        monitor_summary = {
            "gpu_name":          gpu_samples[0]["gpu_name"],
            "vram_total_mb":     gpu_samples[0]["vram_total_mb"],
            "vram_used_mean_mb": _avg("vram_used_mb"),
            "vram_used_peak_mb": _mx("vram_used_mb"),
            "gpu_util_mean_pct": _avg("gpu_util_pct"),
            "gpu_util_peak_pct": _mx("gpu_util_pct"),
            "temp_peak_c":       _mx("temp_c"),
            "power_mean_w":      _avg("power_w"),
            "power_limit_w":     gpu_samples[0]["power_limit_w"],
        }

    if not fps_readings:
        print(f"  [bench] WARNING: no FPS readings captured for n_cams={n_cams}")
        return {
            "inference_interval": inference_interval,
            "n_cams": n_cams,
            "fps_total_mean": 0.0, "fps_total_min": 0.0,
            "fps_total_max": 0.0,  "fps_per_cam": 0.0,
            "n_samples": 0, "wall_seconds": round(wall_time, 1),
            "status": "NO_DATA", **monitor_summary,
        }

    fps_mean    = sum(fps_readings) / len(fps_readings)
    fps_per_cam = fps_mean / n_cams

    return {
        "inference_interval": inference_interval,
        "n_cams":          n_cams,
        "fps_total_mean":  round(fps_mean,          2),
        "fps_total_min":   round(min(fps_readings),  2),
        "fps_total_max":   round(max(fps_readings),  2),
        "fps_per_cam":     round(fps_per_cam,        2),
        "n_samples":       len(fps_readings),
        "wall_seconds":    round(wall_time,          1),
        "status":          "OK",
        **monitor_summary,
    }


def _print_table(results: list[dict], target_fps: float) -> None:
    has_gpu = any("gpu_name" in r for r in results)

    # FPS table
    has_interval = any(r.get("inference_interval", 0) > 0 for r in results)
    iv_col = f"{'Interval':>8}  " if has_interval else ""
    header = (
        f"{'Cams':>5}  {iv_col}{'FPS/cam':>8}  {'FPS total':>10}  "
        f"{'Min':>7}  {'Max':>7}  {'Samples':>7}  {'Pass?':>6}"
    )
    sep = "-" * len(header)
    print()
    print("=" * len(header))
    print(" Throughput benchmark results")
    print("=" * len(header))
    print(header)
    print(sep)
    for r in results:
        ok = "PASS" if r["fps_per_cam"] >= target_fps else "FAIL"
        if r["status"] != "OK":
            ok = r["status"]
        iv = r.get("inference_interval", 0)
        iv_str = f"{'every '+str(iv+1)+'f':>8}  " if has_interval else ""
        print(
            f"{r['n_cams']:>5}  "
            f"{iv_str}"
            f"{r['fps_per_cam']:>8.1f}  "
            f"{r['fps_total_mean']:>10.1f}  "
            f"{r['fps_total_min']:>7.1f}  "
            f"{r['fps_total_max']:>7.1f}  "
            f"{r['n_samples']:>7}  "
            f"{ok:>6}"
        )
    print(sep)

    # GPU table
    if has_gpu:
        g0 = next((r for r in results if "gpu_name" in r), {})
        vram_total = g0.get("vram_total_mb", 0)
        gpu_name   = g0.get("gpu_name", "?")
        print()
        gheader = (
            f"{'Cams':>5}  {'VRAM mean':>10}  {'VRAM peak':>10}  "
            f"{'GPU% mean':>9}  {'GPU% peak':>9}  "
            f"{'Temp°C':>6}  {'Power W':>8}"
        )
        gsep = "-" * len(gheader)
        print(f" GPU: {gpu_name}  ({vram_total} MB total)")
        print(gheader)
        print(gsep)
        for r in results:
            if "vram_used_mean_mb" not in r:
                continue
            print(
                f"{r['n_cams']:>5}  "
                f"{r['vram_used_mean_mb']:>8.0f}MB  "
                f"{r['vram_used_peak_mb']:>8.0f}MB  "
                f"{r['gpu_util_mean_pct']:>9.1f}  "
                f"{r['gpu_util_peak_pct']:>9}  "
                f"{r['temp_peak_c']:>6}  "
                f"{r['power_mean_w']:>8.1f}"
            )
        print(gsep)

    # Summary
    passing = [r for r in results if r["fps_per_cam"] >= target_fps]
    if passing:
        best = passing[-1]
        print(f"\n  Max cameras @ {target_fps} FPS/cam: {best['n_cams']} cameras "
              f"({best['fps_per_cam']:.1f} FPS/cam, "
              f"{best['fps_total_mean']:.1f} FPS total)")
        if "vram_used_peak_mb" in best:
            pct = 100 * best["vram_used_peak_mb"] / max(1, best["vram_total_mb"])
            print(f"  VRAM at that point: {best['vram_used_peak_mb']}/"
                  f"{best['vram_total_mb']} MB ({pct:.0f}%)")
    else:
        print(f"\n  No configuration meets target {target_fps} FPS/cam.")
    print()


def _save_csv(results: list[dict], out_dir: Path) -> Path:
    out_dir.mkdir(parents=True, exist_ok=True)
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    path = out_dir / f"throughput_{ts}.csv"
    if not results:
        return path
    fieldnames = list(results[0].keys())
    with open(path, "w", newline="") as f:
        w = csv.DictWriter(f, fieldnames=fieldnames)
        w.writeheader()
        w.writerows(results)
    return path


def main() -> None:
    p = argparse.ArgumentParser(
        description="Benchmark pipeline throughput vs. number of cameras")
    p.add_argument("--source", required=True, metavar="PATH",
                   help="Video file to use as synthetic source (looped N times)")
    p.add_argument("--cam-counts", nargs="+", type=int,
                   default=[1, 2, 4, 6, 8, 10, 12, 16, 20],
                   help="List of camera counts to test (default: 1 2 4 6 8 10 12 16 20)")
    p.add_argument("--duration", type=int, default=30,
                   help="Measurement window in seconds per run (default: 30)")
    p.add_argument("--warmup", type=int, default=8,
                   help="Warm-up seconds to discard per run (default: 8, "
                        "allows TRT engine to reach steady state)")
    p.add_argument("--target-fps", type=float, default=10.0,
                   help="Per-camera FPS target for pass/fail (default: 10)")
    p.add_argument("--nvinfer-config",
                   default="configs/models/nvinfer_yolov11_mmp_iv4.yml",
                   help="nvinfer config file to use")
    p.add_argument("--tracker-config",
                   default="configs/tracker/nvdcf_perf_mmp_lite.yaml",
                   help="Tracker config file to use for throughput runs")
    p.add_argument("--gpu-id", type=int, default=0)
    p.add_argument("--output-dir", default="output/benchmark",
                   help="Directory to save CSV results (default: output/benchmark)")
    p.add_argument("--stop-on-fail", action="store_true",
                   help="Stop sweeping when per-cam FPS drops below --target-fps")
    p.add_argument("--inference-intervals", nargs="+", type=int, default=[0],
                   help="nvinfer interval values to sweep (default: 0). "
                        "interval=N means inference runs every N+1 frames; "
                        "tracker still runs every frame. "
                        "Example: --inference-intervals 0 1 2 4  "
                        "runs 4 sweeps (every 1f, 2f, 3f, 5f) and compares FPS.")
    args = p.parse_args()

    source = str(Path(args.source).resolve())
    if not Path(source).exists():
        sys.exit(f"[bench] Source not found: {source}")

    # Print GPU info once at start
    gpu_info = _query_gpu(args.gpu_id)
    print(f"[bench] GPU        : {gpu_info['gpu_name']}  "
          f"({gpu_info['vram_total_mb']} MB VRAM)")
    print(f"[bench] Source     : {source}")
    print(f"[bench] Cam counts : {args.cam_counts}")
    print(f"[bench] Intervals  : {args.inference_intervals}  "
          f"(0=every frame, 1=every 2f, 2=every 3f, …)")
    print(f"[bench] Duration   : {args.duration}s + {args.warmup}s warmup per run")
    print(f"[bench] Target FPS : {args.target_fps} per camera")
    print(f"[bench] nvinfer    : {args.nvinfer_config}")
    print(f"[bench] tracker    : {args.tracker_config}")

    results: list[dict] = []

    # auto-escalate mode: when >1 interval given, try next interval on fail
    # instead of running full independent sweeps per interval.
    auto_escalate = len(args.inference_intervals) > 1

    if auto_escalate:
        print(f"[bench] Mode: auto-escalate — retry with next interval on FAIL "
              f"(intervals: {args.inference_intervals})")

        for n in args.cam_counts:
            best_r = None
            for iv in args.inference_intervals:
                r = _run_single(
                    source             = source,
                    n_cams             = n,
                    duration           = args.duration,
                    warmup             = args.warmup,
                    nvinfer_config     = args.nvinfer_config,
                    tracker_config     = args.tracker_config,
                    gpu_id             = args.gpu_id,
                    inference_interval = iv,
                )
                results.append(r)
                ok = "PASS" if r["fps_per_cam"] >= args.target_fps else "FAIL"
                vram = (f"  VRAM peak={r.get('vram_used_peak_mb',0)}MB"
                        f"  GPU={r.get('gpu_util_mean_pct',0):.0f}%"
                        f"  {r.get('temp_peak_c',0)}°C"
                        if "vram_used_peak_mb" in r else "")
                print(f"\n  → iv={iv} cams={n}  FPS/cam={r['fps_per_cam']:.1f}"
                      f"  total={r['fps_total_mean']:.1f}  [{ok}]{vram}\n")
                best_r = r
                if r["fps_per_cam"] >= args.target_fps:
                    break  # passed — no need to try higher intervals
                if iv == args.inference_intervals[-1]:
                    print(f"[bench] cams={n}: all intervals exhausted, still FAIL.")
                else:
                    next_iv = args.inference_intervals[args.inference_intervals.index(iv) + 1]
                    print(f"[bench] cams={n} iv={iv} FAIL — escalating to iv={next_iv} "
                          f"(infer every {next_iv+1}f)")

            if args.stop_on_fail and best_r and best_r["fps_per_cam"] < args.target_fps:
                print(f"[bench] All intervals failed for cams={n} — stopping sweep.")
                break
    else:
        iv = args.inference_intervals[0]
        for n in args.cam_counts:
            r = _run_single(
                source             = source,
                n_cams             = n,
                duration           = args.duration,
                warmup             = args.warmup,
                nvinfer_config     = args.nvinfer_config,
                tracker_config     = args.tracker_config,
                gpu_id             = args.gpu_id,
                inference_interval = iv,
            )
            results.append(r)

            ok = "PASS" if r["fps_per_cam"] >= args.target_fps else "FAIL"
            vram = (f"  VRAM peak={r.get('vram_used_peak_mb',0)}MB"
                    f"  GPU={r.get('gpu_util_mean_pct',0):.0f}%"
                    f"  {r.get('temp_peak_c',0)}°C"
                    if "vram_used_peak_mb" in r else "")
            print(f"\n  → iv={iv} cams={n}  FPS/cam={r['fps_per_cam']:.1f}"
                  f"  total={r['fps_total_mean']:.1f}  [{ok}]{vram}\n")

            if args.stop_on_fail and r["fps_per_cam"] < args.target_fps:
                print(f"[bench] Per-cam FPS {r['fps_per_cam']:.1f} < target "
                      f"{args.target_fps} — stopping cam sweep for interval={iv}.")
                break

    # Final full table
    _print_table(results, args.target_fps)

    csv_path = _save_csv(results, Path(args.output_dir))
    print(f"[bench] Results saved to: {csv_path}")


if __name__ == "__main__":
    main()
